def haversine(lat1, lng1, lat2, lng2):
    """Return great-circle distance in km between two lat/lng points."""
    R = 6371.0
    dlat = math.radians(lat2 - lat1)
    dlng = math.radians(lng2 - lng1)
    a = (math.sin(dlat / 2) ** 2
         + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2))
         * math.sin(dlng / 2) ** 2)
    return R * 2 * math.asin(math.sqrt(a))


def zip_to_coords(zip_str):
    """Return (lat, lng) for a zip/postal code string, or None if unknown."""
    if not zip_str:
        return None
    return ZIP_COORDS.get(zip_str.strip()) or ZIP_COORDS.get(zip_str.strip().upper())


def agent_centroid(agent):
    """Return mean (lat, lng) across the agent's service-area zip codes.

    Returns None when no coordinates can be resolved for any of the agent's
    areas, which causes Phase 2 to rank the agent last (distance = ∞) but
    still include them as a valid candidate ranked by rating.
    """
    zips = [area.zip for area in (agent.areas or [])]
    coords = [c for c in (zip_to_coords(z) for z in zips) if c is not None]
    if not coords:
        return None
    return (
        sum(c[0] for c in coords) / len(coords),
        sum(c[1] for c in coords) / len(coords),
    )
